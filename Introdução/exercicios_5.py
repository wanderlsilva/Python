'''CONSTRUA UM PROGRAMA QUE LEIA 2 NUMEROS REAIS.
CALCULE A SOMA DOS DOIS VALORES E O PRODUTO ENTRE ELES'''

num1 = float(input("Digite o 1º numero: "))
num2 = float(input("Digite o 2º numero: "))

soma = num1 + num2
prod = num1 * num2

print(f"Soma entre os dois numeros é {soma:.2f}")
print(f"Produto entre os dois numeros é {prod:.2f}")