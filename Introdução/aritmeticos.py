#Python oferece diversos conjuntos de operadores que podem ser utilizados em um programa.

#ADIÇÃO
print(2 + 2)

#SUBTRAÇÃO
print(3 - 1)

#MULTIPLICAÇÃO
print(3 * 2)

#DIVISÃO
print(10 / 2)

#QUOCIENTE INTEIRO DA DIVISÃO
print(4 // 3)

#RESTO DA DIVISÃO
print(4 % 2)

#POTENCIAÇÃO
print(4 ** 2)
#OUTRO EXEMPLO DE POTENCIAÇÃO
print(pow(4, 2)) #recebe como entrada valores correspondentes à base e ao expoente

#CONCATENAR (Unir duas ou mais palavras através do sinal de + )
print("Olá "+ "Mundo")

#MULTIPLICAR STRING
print(2 * "ABC")